// finishes.js - Gallery implementation
document.addEventListener('DOMContentLoaded', function() {
    // DOM Elements
    const galleryContainer = document.getElementById('gallery');
    const modal = document.getElementById('imageModal');
    const modalImage = document.getElementById('modalImage');
    const modalTitle = document.getElementById('modalTitle');
    const modalDescription = document.getElementById('modalDescription');
    const modalFeatures = document.getElementById('modalFeatures');
    const modalApplications = document.getElementById('modalApplications');
    const closeBtn = document.querySelector('.close-btn');
    const prevBtn = document.querySelector('.prev-btn');
    const nextBtn = document.querySelector('.next-btn');
    
    let currentImageIndex = 0;
    
    // Initialize the gallery
    function initGallery() {
        renderGalleryItems();
        setupEventListeners();
    }
    
    // Render gallery items from data
    function renderGalleryItems() {
        galleryContainer.innerHTML = '';
        
        galleryData.forEach((item, index) => {
            const galleryItem = document.createElement('div');
            galleryItem.className = 'gallery-item';
            galleryItem.dataset.index = index;
            
            galleryItem.innerHTML = `
                <div class="gallery-image-container">
                    <img 
                        src="${item.imageSrc}" 
                        alt="${item.alt}" 
                        class="gallery-image"
                        loading="lazy"
                    >
                </div>
                <div class="gallery-caption">
                    <h3>${item.title}</h3>
                    <p>${item.description}</p>
                </div>
            `;
            
            galleryContainer.appendChild(galleryItem);
        });
    }
    
    // Set up event listeners
    function setupEventListeners() {
        // Gallery item click
        galleryContainer.addEventListener('click', function(e) {
            const galleryItem = e.target.closest('.gallery-item');
            if (galleryItem) {
                currentImageIndex = parseInt(galleryItem.dataset.index);
                openModal(currentImageIndex);
            }
        });
        
        // Close modal
        closeBtn.addEventListener('click', closeModal);
        modal.addEventListener('click', function(e) {
            if (e.target === modal) {
                closeModal();
            }
        });
        
        // Navigation
        prevBtn.addEventListener('click', showPreviousImage);
        nextBtn.addEventListener('click', showNextImage);
        
        // Keyboard navigation
        document.addEventListener('keydown', function(e) {
            if (!modal.classList.contains('show')) return;
            
            if (e.key === 'Escape') {
                closeModal();
            } else if (e.key === 'ArrowLeft') {
                showPreviousImage();
            } else if (e.key === 'ArrowRight') {
                showNextImage();
            }
        });
    }
    
    // Open modal with specific image
    function openModal(index) {
        updateModalContent(index);
        modal.classList.add('show');
        document.body.style.overflow = 'hidden';
    }
    
    // Close modal
    function closeModal() {
        modal.classList.remove('show');
        setTimeout(() => {
            modal.style.display = 'none';
        }, 300);
        document.body.style.overflow = '';
    }
    
    // Update modal content with smooth transition
    function updateModalContent(index) {
        const item = galleryData[index];
        
        // Apply transition effect
        modalImage.style.opacity = '0';
        modalImage.style.transform = 'translateX(30px)';
        
        setTimeout(() => {
            modalImage.src = item.imageSrc;
            modalTitle.textContent = item.title;
            modalDescription.textContent = item.description;
            modalFeatures.innerHTML = item.features.map(feature => `<li>${feature}</li>`).join('');
            modalApplications.innerHTML = item.applications.map(application => `<li>${application}</li>`).join('');
            // Reveal with animation
            modalImage.style.opacity = '1';
            modalImage.style.transform = 'translateX(0)';
        }, 300);
        
        modal.style.display = 'flex';
    }
    
    // Show previous image
    function showPreviousImage() {
        currentImageIndex = (currentImageIndex - 1 + galleryData.length) % galleryData.length;
        updateModalContent(currentImageIndex);
    }
    
    // Show next image
    function showNextImage() {
        currentImageIndex = (currentImageIndex + 1) % galleryData.length;
        updateModalContent(currentImageIndex);
    }
    
    // Add touch swipe support for mobile
    let touchStartX = 0;
    let touchEndX = 0;
    
    modal.addEventListener('touchstart', function(e) {
        touchStartX = e.changedTouches[0].screenX;
    }, false);
    
    modal.addEventListener('touchend', function(e) {
        touchEndX = e.changedTouches[0].screenX;
        handleSwipe();
    }, false);
    
    function handleSwipe() {
        const swipeThreshold = 50;
        if (touchEndX < touchStartX - swipeThreshold) {
            showNextImage();
        }
        if (touchEndX > touchStartX + swipeThreshold) {
            showPreviousImage();
        }
    }
    
    // Initialize the gallery
    initGallery();
});
